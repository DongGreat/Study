X = 6
Y = 2

if X > Y:
    print("아주 잘했어요!")

# X < Y - X가 Y보다 작다
# X > Y - X가 Y보다 크다
# X == Y - X와 Y가 같다
# X != Y - X와 Y가 같지 않다
# X >= Y - X가 Y보다 크거나 같다
# X <= Y - X가 Y보다 작거나 같다

X = 10

if (X < 8) or (X > 2):
    print("정말 잘했어요!")

# A or B - A와 B 둘 중에 하나만 참이면 참
# A and B - A와 B 모두가 참이어야 참

if not (X < 8):
    print("정말 잘했어요!")

# not A - A가 거짓이면 참